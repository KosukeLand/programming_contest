package main

import (
	"fmt"
	"math"
	"sort"
)

func main() {
	var N, X, ans int

	fmt.Scan(&N, &X)
	var m = make(SortBy, N)

	for i := 0; i < N; i++ {
		fmt.Scan(&m[i])
		X -= m[i]
	}
	ans += N
	sort.Sort(m)

	ans += floor((X / m[0]))
	fmt.Println(ans)
}

