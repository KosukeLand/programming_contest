package main

import (
	"fmt"
	"math"
	"sort"
)

func main() {
	var N, ans int
	fmt.Scan(&N)

	A := make([]int, N)
	for i := 0; i < N; i++ {
		fmt.Scan(&A[i])
	}
	sort.Sort(SortBy(A))
	for i := 0; i < N-1; i++ {
		if A[i] < 0 {
			A[i] *= (-1)
			A[i+1] *= (-1)
		}
		ans += A[i]
	}
	fmt.Println(ans + A[N-1])
}

